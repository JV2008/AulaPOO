package ProgramacaoOO;

public class Aviao {
	//Atributos
	String modelo;
	String quilometragem;
	int capacidade;
	int motores;
	//Métodos
	String direção;
	String velocidademax;
	String tamanho;
	String voar;

	// declaração de atributos
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getQuilometragem() {
		return quilometragem;
	}
	public void setQuilometragem(String quilometragem) {
		this.quilometragem = quilometragem;
	}
	public int getCapacidade() {
		return capacidade;
	}
	public void setCapacidade(int capacidade) {
		this.capacidade = capacidade;
	}
	public int getMotores() {
		return motores;
	}
	public void setMotores(int motores) {
		this.motores = motores;
	}
	// declaração dos métodos
	public String getDireção() {
		return direção;
	}
	public void setDireção(String direção) {
		this.direção = direção;
	}
	public String getVelocidademax() {
		return velocidademax;
	}
	public void setVelocidademax(String velocidademax) {
		this.velocidademax = velocidademax;
	}
	public String getTamanho() {
		return tamanho;
	}
	public void setTamanho(String tamanho) {
		this.tamanho = tamanho;
	}
	public String getVoar() {
		return voar;
	}
	public void setVoar(String voar) {
		this.voar = voar;
	}


}




