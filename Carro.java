package ProgramacaoOO;

public class Carro {
    
	
	//Atributos
	String modelo;
	String quilometragem;
	String cor;
	String placa;
	//Métodos
	String direção;
	String velocidademax;
	String motor;
	String seta;

	// declaração de atributos
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getQuilometragem() {
		return quilometragem;
	}
	public void setQuilometragem(String quilometragem) {
		this.quilometragem = quilometragem;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	// declaração dos métodos
	public String getDireção() {
		return direção;
	}
	public void setDireção(String direção) {
		this.direção = direção;
	}
	public String getVelocidademax() {
		return velocidademax;
	}
	public void setVelocidademax(String velocidademax) {
		this.velocidademax = velocidademax;
	}
	public String getMotor() {
		return motor;
	}
	public void setMotor(String motor) {
		this.motor = motor;
	}
	public String getSeta() {
		return seta;
	}
	public void setSeta(String seta) {
		this.seta = seta;
	}

	



}
