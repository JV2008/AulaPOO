package ProgramacaoOO;

public class Animal {
		//Atributos
		String nome;
		String familia;
		int idade;
		int peso;
		//Métodos
		String anda;
		String dorme;
		String som;
		String come;

		// declaração de atributos
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public String getFamilia() {
			return familia;
		}
		public void setFamilia(String familia) {
			this.familia = familia;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		}
		public int getPeso() {
			return peso;
		}
		public void setPeso(int peso) {
			this.peso = peso;
		}
		// declaração dos métodos
		public String getAnda() {
			return anda;
		}
		public void setAnda(String anda) {
			this.anda = anda;
		}
		public String getDorme() {
			return dorme;
		}
		public void setDorme(String dorme) {
			this.dorme = dorme;
		}
		public String getSom() {
			return som;
		}
		public void setSom(String som) {
			this.som = som;
		}
		public String getCome() {
			return come;
		}
		public void setCome(String come) {
			this.come = come;
		}
	
			
		}









